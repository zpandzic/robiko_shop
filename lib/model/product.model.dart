class Product {
  final String imageUrl;
  final String name;
  final String code;
  final String catalogNumber;
  final String description;
  final String category;
  final double price;

  Product({
    required this.imageUrl,
    required this.name,
    required this.code,
    required this.catalogNumber,
    required this.description,
    required this.category,
    required this.price,
  });
}
